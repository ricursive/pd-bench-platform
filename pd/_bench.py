import os
import pathlib
import sys

# Allow override (e.g. the framework's future `pdbench` package); default to the
# submodule's per-task tests dir where def_checks/scoring/config currently live.
_DEFAULT = (
    pathlib.Path(__file__).resolve().parent.parent
    / "vendor" / "pd-bench" / "tasks" / "ricursive"
    / "ariane133-asap7-mixed-placement" / "tests"
)
_BENCH = pathlib.Path(os.environ.get("PDBENCH_TESTS_DIR", _DEFAULT))
if str(_BENCH) not in sys.path:
    sys.path.insert(0, str(_BENCH))

import def_checks  # noqa: E402  (from the submodule)
import scoring  # noqa: E402

BENCH_TESTS_DIR = _BENCH
__all__ = ["def_checks", "scoring", "BENCH_TESTS_DIR"]
